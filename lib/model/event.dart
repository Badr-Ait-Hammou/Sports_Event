class Event {
  final String name;
  final String date;
  final String time;
  final String location;
  final String sportType;
  final int participants;
  final String rules;

  Event({
    required this.name,
    required this.date,
    required this.time,
    required this.location,
    required this.sportType,
    required this.participants,
    required this.rules,
  });
}
